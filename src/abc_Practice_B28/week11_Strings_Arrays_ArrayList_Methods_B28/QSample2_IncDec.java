package abc_Practice_B28.week11_Strings_Arrays_ArrayList_Methods_B28;

public class QSample2_IncDec {
    public static void main(String[] args) {
        int x;
        int y;


        x = 2;
        y = x++;
        System.out.println("y = " + y);

        x = 2;
        System.out.println("x++ = " + x++);

        x = 2;
        System.out.println("--x = " + --x);

        x = 8;
        y = x--;
        System.out.println("y = " + y);


    }



}
         /*
         y = 2
        x++ = 2
        --x = 1
        y = 8
          */