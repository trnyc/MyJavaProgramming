package quizzes_B_28.ifstatements;

public class Q13_Switch {

    public static void main(String[] args) {

        int x= 10;
        switch (x)  {
            case 10:
                System.out.println("Monday");
            case 11:
                System.out.println("Tuesday");
            default:
                System.out.println("Friday");
                // Monday Tuesday Friday
        }



    }




}
