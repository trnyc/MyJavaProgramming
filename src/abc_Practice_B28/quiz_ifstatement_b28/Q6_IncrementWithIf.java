package quizzes_B_28.ifstatements;

public class Q6_IncrementWithIf {
    public static void main(String[] args) {
        int num= 10;

        if(--num==10 ) {
            System.out.println("Hello Cydeo " + num);
        } else {
            System.out.println("Hello World " + num);
        }

        // output = Hello World 9

    }





}
