public class Diamond2 {
    public static void main(String[] args) {
        System.out.println("\t    ^");
        System.out.println("\t   / \\");
        System.out.println("\t  / * \\");
        System.out.println("\t / * * \\");
        System.out.println("\t/ * * * \\");
        System.out.println("\t----------");
        System.out.println("\t\\*  *  */");
        System.out.println("\t \\ * * /");
        System.out.println("\t  \\ * /");
        System.out.println("\t   \\ /");
        System.out.println("\t     v");
    }



}
